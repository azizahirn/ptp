            <p>
              Poetry is recognised as one of the oldest mediums to preserve culture as it is rich in emotion, sensitivity and imagination. Through poetry, human and cultural values can be captured and passed to the next generation. Realising this, Poetic Trails of Perlis competition aims to promote and sustain the cultural, natural and community sites in Perlis<span id="dots">...</span><span id="more">

              <br>
              <br>
              This multilingual poetry film competition project uses poetry film to rediscover cultural sites in Perlis to create positive experiences for visitors, sustain cultural site and generate economic activities for the local community. We welcome emerging Malaysian poets (above 18 years old) to join us for this competition. A one-day guided site visit will be organized for all participants where they can draw inspiration and at the same time indulge in the culture and beautiful nature of Perlis.

              <br>
              <br>
              This competition is sponsored by the Cultural Economy Catalytic Grants 2nd Cycle. It is a grant programme that promotes the Northern Region of Peninsular Malaysia’s unique tangible and intangible cultural assets as enablers for economic resiliency and socio-economic growth, while highlighting the nation’s rich heritage and history. The Cultural Economy Catalytic Grants is part of the Northern Region Archaeotourism Network & Local Economic Development, a Think City programme under the auspices of the Ministry of Finance.
